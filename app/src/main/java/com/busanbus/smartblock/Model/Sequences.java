package com.busanbus.smartblock.Model;

public class Sequences {
    private int userSeq;
    private int adminSeq;
    private int groupUserSeq;
    private int clientGroupSeq;

    public int getUserSeq() {
        return userSeq;
    }

    public void setUserSeq(int userSeq) {
        this.userSeq = userSeq;
    }

    public int getAdminSeq() {
        return adminSeq;
    }

    public void setAdminSeq(int adminSeq) {
        this.adminSeq = adminSeq;
    }

    public int getGroupUserSeq() {
        return groupUserSeq;
    }

    public void setGroupUserSeq(int groupUserSeq) {
        this.groupUserSeq = groupUserSeq;
    }

    public int getClientGroupSeq() {
        return clientGroupSeq;
    }

    public void setClientGroupSeq(int clientGroupSeq) {
        this.clientGroupSeq = clientGroupSeq;
    }
}
